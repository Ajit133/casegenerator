            <div class="faq-item" data-category="technical">
                <div class="faq-question">
                    <h3>What are the common problems of generators?</h3>
                    <span class="faq-toggle">+</span>
                </div>
                <div class="faq-answer">
                    <p>Generators can experience several common issues:<br><br>
                    • <strong>Fuel-related:</strong> Clogged filters, low or stale fuel.<br>
                    • <strong>Electrical:</strong> Damaged alternators, failing regulators, or worn spark plugs.<br>
                    • <strong>Engine:</strong> Seized engine, failing oil pump, or worn cylinders.<br>
                    • <strong>Cooling:</strong> Clogged radiator, failing water pump, or low coolant.<br>
                    • <strong>Exhaust:</strong> Clogged muffler or damaged pipes.<br>
                    • <strong>Control Panel:</strong> Faulty circuit breakers or control boards.<br>
                    • <strong>Starting:</strong> Dead battery or failing starter motors.</p>
                </div>
            </div>

