                    • <strong>Cooling & Exhaust:</strong> Overheating due to radiator clogs or exhaust blockages.<br>
                    • <strong>Electrical:</strong> Alternator failures or voltage regulator issues.<br>
                    • <strong>Starting:</strong> Battery or starter motor failures.</p>
